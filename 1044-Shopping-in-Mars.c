#include<stdio.h>
#include<stdlib.h>
int main()
{
	int *value,i,j,n,m,temp,total=0,min=0,flag=1;
	scanf("%d%d",&n,&m);
	value=(int *)malloc((n+1)*sizeof(int));
	value[0]=0;
	for(i=0;i<n;i++)
	{
		scanf("%d",&temp);
		total+=temp;
		value[i+1]=total;
	}
	i=0;
	j=1;
	while(i<n&&j<=n)
	{
		while(value[j]-value[i]<m&&i<n&&j<=n)
		{
			j++;
		}
		if(value[j]-value[i]==m)
		{
			printf("%d-%d\n",i+1,j);
			flag=0;
			i++;
		}
		while(value[j]-value[i]>m&&i<n&&j<=n)
		{
			if(min==0||value[j]-value[i]-m<min)
			{
				min=value[j]-value[i]-m;
			}
			i++;
		}
	}
	i=0;
	j=1;
	if(flag)
	{
		while(i<n&&j<=n)
		{
			while(value[j]-value[i]<m+min&&i<n&&j<=n)
			{
				j++;
			}
			if(value[j]-value[i]==m+min)
			{
				printf("%d-%d\n",i+1,j);
				i++;
			}
			while(value[j]-value[i]>m+min&&i<n&&j<=n)
			{
				i++;
			}
		}
	}
}